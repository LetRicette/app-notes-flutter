class Note {
  final String id;
  final String title;
  final String text;
  final String date;
  final bool favorite;
  final bool attachment;
  final String deadline;
  final String color;

  Note({
    required this.id,
    required this.title,
    required this.text,
    required this.date,
    required this.favorite,
    required this.attachment,
    required this.deadline,
    required this.color,
  });
}
